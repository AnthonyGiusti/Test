public class Mur {          //Un mur vaut 3 dans le jeu
    private int x;
    private  int y;

    public Mur(String nom, int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }


}